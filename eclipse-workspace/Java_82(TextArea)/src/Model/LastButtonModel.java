package Model;

public class LastButtonModel {
	private int value;
	

	public LastButtonModel() {
		this.value = 1;
	}

	/**
	 * @return the value
	 */
	public int getValue() {
		return value;
	}

	/**
	 * @param value the value to set
	 */
	public void setValue(int value) {
		this.value = value;
	}
	
	public void setValue_to_1() {
		this.setValue(1);
	}
	public void setValue_to_2() {
		this.setValue(2);
	}
	public void setValue_to_3() {
		this.setValue(3);
	}
	public void setValue_to_4() {
		this.setValue(4);
	}
	
	
}
